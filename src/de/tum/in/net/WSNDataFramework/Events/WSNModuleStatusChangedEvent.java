package de.tum.in.net.WSNDataFramework.Events;

import de.tum.in.net.WSNDataFramework.WSNModule;
import de.tum.in.net.WSNDataFramework.WSNModule.WSNModuleStatus;

/**
 * Event that gets fired each time a WSNModule changes its status.
 * 
 * @author Andr√© Freitag
 */
public class WSNModuleStatusChangedEvent extends WSNEvent {
	
	public WSNModule module;
	public WSNModuleStatus status;
	
	
	public WSNModuleStatusChangedEvent(WSNModule module, WSNModuleStatus status) {
		this.module = module;
		this.status = status;
	}

}
