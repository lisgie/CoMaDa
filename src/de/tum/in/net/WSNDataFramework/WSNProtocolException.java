package de.tum.in.net.WSNDataFramework;

/**
 * Represents an Exception thrown by the WSN.
 * Wrapper around java.lang.Exception.
 * 
 * @author Andr√© Freitag
 */
public class WSNProtocolException extends WSNException {

	public WSNProtocolException() {

	}
	public WSNProtocolException(String message) {
		super(message);
	}
	public WSNProtocolException(String message, Throwable cause) {
		super(message, cause);
	}
	public WSNProtocolException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = -3207118699438271861L;

}
